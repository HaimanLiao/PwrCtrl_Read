static SIGNAL_FUN sgfun[] = {
	{ NULL, NULL }
};

static METHOD_FUN mdfun[] = {
	{NULL, NULL},
	{NULL, NULL}
};

static MSG_MONITOR motlist = {
	sgfun,
	mdfun
};

int DBUS_Init()
{
	if (dbs_connect_init("PWRCTRL", &motlist) < 0)
	{
		DEBUG("dbus_connect_init error!");
		return -1;
	}

	return 0;
}

void DBUS_Uninit()
{
	dbs_connect_uninit();
}

